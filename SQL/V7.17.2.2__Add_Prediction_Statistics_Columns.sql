ALTER TABLE Prediction

ADD COLUMN Duration bigint(20) GENERATED ALWAYS AS 
	(CASE WHEN ((`StartTime` > '1985-11-23') AND (`EndTime` > '1985-11-23'))
		THEN (TIMESTAMPDIFF(MICROSECOND, `StartTime`, `EndTime`) / 1000)
		ELSE -(1) 
	END) VIRTUAL AFTER `Error`,
ADD COLUMN IsSuccess bit(1) GENERATED ALWAYS AS (CASE WHEN ISNULL(Error) THEN TRUE ELSE FALSE END) VIRTUAL AFTER `Duration`,
ADD COLUMN IsError bit(1) GENERATED ALWAYS AS (CASE WHEN (Error IS NOT NULL) THEN TRUE ELSE FALSE END) VIRTUAL AFTER `IsSuccess`;